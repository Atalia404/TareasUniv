# Se necesita obtener el promedio simple de un estudiante a partir de sus tres notas parciales N1, N2 y N3.

note_1 = int(input("Ingrese la PRIMERA nota obtenida: "))
note_2 = int(input("Ingrese la SEGUNDA nota obtenida: "))
note_3 = int(input("Ingrese la TERCERA nota obtenida: "))
average = (note_1+note_2+note_3)/3
print(f"Su promedio de notas es de: {average} puntos.")
